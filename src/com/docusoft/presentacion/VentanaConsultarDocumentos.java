    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        String texto = textoDeBusqueda.getText().trim();
        CatalogoDeDocumentos catalogoDeDocumentos = CatalogoDeDocumentos.obtenerInstancia();       
        Object[] columnas = new Object[]{"Código", "Título", "Descripción", "Fecha"};
        DefaultTableModel modeloTabla = new DefaultTableModel(catalogoDeDocumentos.buscarDocumentos(texto), columnas);
        tablaDocumentos.setModel(modeloTabla);
        configurarTablaDocumentos();
    }//GEN-LAST:event_botonBuscarActionPerformed

