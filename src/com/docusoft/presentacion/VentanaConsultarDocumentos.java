    private void consultarTodosLosDocumentos() {
        CatalogoDeDocumentos catalogoDeDocumentos = CatalogoDeDocumentos.obtenerInstancia();
        Object[] columnas = new Object[]{"Código", "Título", "Descripción", "Fecha"};
        DefaultTableModel modeloTabla = new DefaultTableModel(catalogoDeDocumentos.obtenerDocumentos(), columnas);
        tablaDocumentos.setModel(modeloTabla);
        configurarTablaDocumentos();
    }

    private void configurarTablaDocumentos() {
        TableColumn columnaCodigo = tablaDocumentos.getColumnModel().getColumn(0);
        columnaCodigo.setPreferredWidth(70);
        columnaCodigo.setMaxWidth(100);
        columnaCodigo.setMinWidth(70);
        TableColumn columnaTitulo = tablaDocumentos.getColumnModel().getColumn(1);
        columnaTitulo.setPreferredWidth(350);
        columnaTitulo.setMaxWidth(500);
        columnaTitulo.setMinWidth(100);
        TableColumn columnaDescripcion = tablaDocumentos.getColumnModel().getColumn(2);
        columnaDescripcion.setPreferredWidth(500);
        columnaDescripcion.setMaxWidth(800);
        columnaDescripcion.setMinWidth(100);
        TableColumn columnaFecha = tablaDocumentos.getColumnModel().getColumn(3);
        columnaFecha.setPreferredWidth(90);
        columnaFecha.setMaxWidth(110);
        columnaFecha.setMinWidth(90);
    }

