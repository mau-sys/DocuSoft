    private void botonVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVerActionPerformed
        int fila = tablaDocumentos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un documento de la tabla", "DocuSoft", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String codigoDocumento = (String)tablaDocumentos.getValueAt(fila, 0);
        VentanaVerDocumento ventanaVerDocumento = new VentanaVerDocumento(this, codigoDocumento);
        ventanaVerDocumento.setVisible(true);
    }//GEN-LAST:event_botonVerActionPerformed

